package context.convertion;

public class State {
	private String stateType;
	private String stateURI;
	private String stateName;
	/**
	 * @return the stateType
	 */
	public String getStateType() {
		return stateType;
	}
	/**
	 * @param stateType the stateType to set
	 */
	public void setStateType(String stateType) {
		this.stateType = stateType;
	}
	
	/**
	 * @return the stateName
	 */
	public String getStateName() {
		return stateName;
	}
	/**
	 * @param stateName the stateName to set
	 */
	public void setStateName(String stateName) {
		this.stateName = stateName;
	}
	/**
	 * @return the stateURI
	 */
	public String getStateURI() {
		return stateURI;
	}
	/**
	 * @param stateURI the stateURI to set
	 */
	public void setStateURI(String stateURI) {
		this.stateURI = stateURI;
	}

}
